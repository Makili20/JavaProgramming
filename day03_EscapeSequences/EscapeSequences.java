package day03_EscapeSequences;
/*
escape sequences: MUST be given with double quote
\n: starts a new line
\t: tab
\\: to print one backslash
\": to print double quote
 */
public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Java \nPython \nC#");

        System.out.println("----------------------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to see you all! \nWhat classes do we have next week?");

        System.out.println("--------------------");
        System.out.println("\tJava is Cool Programming language");

        System.out.println("--------------------");

        System.out.println("/ \\");

        System.out.println("-----------------");

        System.out.println("My favourite TV show is \"Game of Throne\"");

    }



}
